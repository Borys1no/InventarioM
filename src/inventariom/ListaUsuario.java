    public void MostrarUsuarios() {
       try {
            DefaultTableModel modelo = new DefaultTableModel();
            tbUsuarios.setModel(modelo);

            PreparedStatement ps = null;
            ResultSet rs = null;

            Conexion conn = new Conexion();
            Connection con = conn.getConnection();

            String query = "SELECT nombres, apellidos, usuario, correo FROM usuario;";
            ps = con.prepareStatement(query);
             rs = ps.executeQuery();
             ResultSetMetaData rsMd = rs.getMetaData();
             int cantidadColumnas = rsMd.getColumnCount();
             
             modelo.addColumn("Nombres");
             modelo.addColumn("Apellidos");
             modelo.addColumn("Usuario");
             modelo.addColumn("Correo");
             
             while(rs.next()){
                 Object[] filas = new Object[cantidadColumnas];
                 for(int i=0; i<cantidadColumnas; i++){
                     filas[i] = rs.getObject(i+1);
                 }
                 modelo.addRow(filas);
             }
             

        } catch (SQLException e) {
            java.util.logging.Logger.getLogger(InventarioSalida.class.getName()).log(java.util.logging.Level.SEVERE, null, e);
        } 
    }
    
    public void InsertarUsuarios(){
        try {
            PreparedStatement ps = null;
            Conexion conn = new Conexion();
            Connection con = conn.getConnection();
            String query = "INSERT INTO usuario (nombres, apellidos, usuario, correo, password) VALUES(?,?,?,?,?);";

            ps = con.prepareStatement(query);

            
            String Nombres = (txtNombres.getText());
            String Apellidos = (txtApellidos.getText());
            String Usuario = (txtUsuario.getText());
            String Correo =(txtCorreo.getText());
            char[] password = txtPass.getPassword();
            String pass = new String(password);
          

            ps.setString(1, Nombres);
            ps.setString(2, Apellidos);
            ps.setString(3, Usuario);
            ps.setString(4, Correo);
            ps.setString(5, pass);
          

            ps.execute();

            JOptionPane.showMessageDialog(null, "Se realizÃ³ el registro correctamente!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al completar el registro, error:" + e.toString());

        }
    }
